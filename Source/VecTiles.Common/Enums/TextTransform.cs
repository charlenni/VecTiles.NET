namespace VecTiles.Common.Enums;

/// <summary>
/// Specifies text transformation options.
/// </summary>
public enum TextTransform
{
    /// <summary>
    /// No transformation is applied.
    /// </summary>
    None,
    /// <summary>
    /// Converts all characters to uppercase.
    /// </summary>
    Uppercase,
    /// <summary>
    /// Converts all characters to lowercase.
    /// </summary>
    Lowercase
}
