namespace VecTiles.Common.Enums;

/// <summary>
/// Specifies how text should be justified within its container.
/// </summary>
public enum TextJustify
{
    /// <summary>
    /// Automatically determine the best justification.
    /// </summary>
    Auto,
    /// <summary>
    /// Align text to the left.
    /// </summary>
    Left,
    /// <summary>
    /// Center the text.
    /// </summary>
    Center,
    /// <summary>
    /// Align text to the right.
    /// </summary>
    Right
}
